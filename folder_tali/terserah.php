function hitungdiskon($totalbelanja) {
    $diskon =0 ;
    if ($totalbelanja >=100000) {
        $diskon = 0.1 * $totalbelanja;
    } elseif ($totalbelanja >=50000) {
        $diskon = 0.05 * $totalbelanja;
    }
    return $diskon;
    
}
